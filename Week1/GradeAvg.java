package Week1;

public class GradeAvg {
	
	public static void main(String[] args) {
		
		//declare and initialize all quiz scores
		double quizScore1 = 87;
		double quizScore2 = 59;
		double quizScore3 = 72;
		double quizScore4 = 85;
		double quizScore5 = 95;
		double quizScore6 = 78;
		double quizScore7 = 79;
		
		//declare and initialize the average of the 7 scores
		double average = (quizScore1 +  quizScore2 +  quizScore3 +  quizScore4 +  quizScore5 +  quizScore6 + quizScore7)/7;
		
		//Output the labeled system average
		System.out.println("The average is: " + average);
		
	}
	
}
